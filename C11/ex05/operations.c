int	do_sum(int a, int b)
{
	return (a + b);
}

int	do_minus(int a, int b)
{
	return (a - b);
}

int	do_div(int a, int b)
{
	return (a / b);
}

int	do_multi(int a, int b)
{
	return (a * b);
}

int	do_mod(int a, int b)
{
	return (a % b);
}
